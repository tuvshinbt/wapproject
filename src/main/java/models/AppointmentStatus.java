package models;

public class AppointmentStatus {
	private int id;
	private String Name;

	// #region [getter setter]
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}
	// #endregion
}
